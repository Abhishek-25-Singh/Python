#Type Function And Data Types in Python 

print("Type of 4 :", type (4))
print("Type of 4.0  :",type(4.0))
print("Type of '4' :",type('4'))
print("Type of 'A' :",type('A'))
print("Type of \"ABC\":",type("ABC"))
print("Type of 4j :",type(4j))

